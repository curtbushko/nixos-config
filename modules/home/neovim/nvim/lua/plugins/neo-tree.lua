          require("neo-tree.command").execute({ toggle = true, dir = vim.uv.cwd() })
        end,
        desc = " Explorer (cwd)",
      },
      { "<leader>e", "<leader>fe", desc = " Explorer (root dir)", remap = true },
      { "<leader>E", "<leader>fE", desc = " Explorer (cwd)", remap = true },
